    folded_matrix = raw
    if not os.path.exists(CANDIDATE_IDS_PATH):
        raise FileNotFoundError(
            "results/candidate_ids.txt not found alongside the old-format "
            "folded_candidates.npy. Re-run fold_all_candidates() to regenerate."
        )
    with open(CANDIDATE_IDS_PATH, "r") as f:
        candidate_ids = f.read().strip().split("\n")
    return folded_matrix, candidate_ids, None


