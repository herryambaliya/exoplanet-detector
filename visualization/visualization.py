def plot_confidence(tic_id, save=True):
    """
    Plots a horizontal bar chart of a candidate's classification
    confidence across all 4 classes (planet, eclipsing_binary, blend,
    other), from M3's results/predictions.csv.

    Args:
        tic_id (str): TIC ID to plot
        save (bool): if True, saves to results/plots/confidence_{tic_id}.png

    Returns:
        matplotlib Figure object
    """
    setup_style()

    if not os.path.exists(PREDICTIONS_PATH):
        raise FileNotFoundError(
            "results/predictions.csv not found. Run M3's predict.py first."
        )

    row = None
    with open(PREDICTIONS_PATH, "r") as f:
        reader = csv.DictReader(f)
        for r in reader:
            if r["TIC_ID"] == str(tic_id):
                row = r
                break

    if row is None:
        raise ValueError(f"TIC {tic_id} not found in predictions.csv")

    classes = ["planet", "binary", "blend", "other"]
    probs = [
        float(row.get("planet_prob", 0)),
        float(row.get("binary_prob", 0)),
        float(row.get("blend_prob", 0)),
        float(row.get("other_prob", 0)),
    ]
    colors = ["#2ecc71" if c == "planet" else "#95a5a6" for c in classes]

    fig, ax = plt.subplots(figsize=(7, 3.5))
    ax.barh(classes, probs, color=colors)
    ax.set_xlim(0, 1)
    ax.set_xlabel("Confidence")

    predicted = row.get("predicted_class", "unknown")
    max_prob = max(probs)
    ax.set_title(f"TIC {tic_id} — Classification: {predicted} ({max_prob*100:.0f}% confidence)")

    for i, p in enumerate(probs):
        ax.text(p + 0.02, i, f"{p*100:.0f}%", va="center", fontsize=10)

    plt.tight_layout()

    if save:
        os.makedirs(PLOTS_DIR, exist_ok=True)
        out_path = os.path.join(PLOTS_DIR, f"confidence_{tic_id}.png")
        plt.savefig(out_path, dpi=150)
        print(f"Saved {out_path}")

    return fig


def plot_confusion_matrix(save=True):
    """
    Plots the model's confusion matrix from M3's results/metrics.json
    -- a heatmap showing how often the CNN's predictions matched the
    true labels on the test set.

    Args:
        save (bool): if True, saves to results/plots/confusion_matrix.png

    Returns:
        matplotlib Figure object
    """
    setup_style()
    import seaborn as sns

    if not os.path.exists(METRICS_PATH):
        raise FileNotFoundError(
            "results/metrics.json not found. Run M3's evaluate_on_test_set() first."
        )

    with open(METRICS_PATH, "r") as f:
        metrics = json.load(f)

    if "confusion_matrix" not in metrics:
        raise KeyError(
            "metrics.json does not contain a 'confusion_matrix' key. "
            "Check M3's evaluate_on_test_set() output format matches "
            "what this function expects."
        )

    cm = np.array(metrics["confusion_matrix"])
    labels = ["Planet", "Binary", "Blend", "Other"]

    fig, ax = plt.subplots(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Greens",
                xticklabels=labels, yticklabels=labels, ax=ax)
    ax.set_xlabel("Predicted class")
    ax.set_ylabel("True class")
    ax.set_title("Model classification results (test set)")
    plt.tight_layout()

    if save:
        os.makedirs(PLOTS_DIR, exist_ok=True)
        out_path = os.path.join(PLOTS_DIR, "confusion_matrix.png")
        plt.savefig(out_path, dpi=150)
        print(f"Saved {out_path}")

    return fig


def plot_all(top_n=3):
    """
    Convenience function: plots raw + folded views for the top N
    candidates (by BLS power score), plus the full candidates grid.
    Stage 2 plots (confidence, confusion matrix) are skipped
    automatically if M3's output doesn't exist yet.

    Args:
        top_n (int): how many top candidates to plot in detail
    """
    if not os.path.exists(CANDIDATES_PATH):
        print("No candidates.csv found. Run M2's detect.py first.")
        return

    with open(CANDIDATES_PATH, "r") as f:
        reader = csv.DictReader(f)
        candidates = list(reader)

    candidates_sorted = sorted(candidates, key=lambda r: float(r["power"]), reverse=True)
    top_candidates = candidates_sorted[:top_n]

    for c in top_candidates:
        tic_id = c["TIC_ID"]
        try:
            plot_raw_lightcurve(tic_id)
            plot_folded_transit(tic_id)
        except Exception as e:
            print(f"Could not plot TIC {tic_id}: {e}")

    try:
        plot_all_candidates_grid()
    except Exception as e:
        print(f"Could not plot candidates grid: {e}")

    if os.path.exists(PREDICTIONS_PATH):
        for c in top_candidates:
            try:
                plot_confidence(c["TIC_ID"])
            except Exception as e:
                print(f"Could not plot confidence for TIC {c['TIC_ID']}: {e}")
    else:
        print("Skipping confidence plots -- predictions.csv not yet available (waiting on M3)")

    if os.path.exists(METRICS_PATH):
        try:
            plot_confusion_matrix()
        except Exception as e:
            print(f"Could not plot confusion matrix: {e}")
    else:
        print("Skipping confusion matrix -- metrics.json not yet available (waiting on M3)")


if __name__ == "__main__":
    plot_all()
