def plot_folded_transit(tic_id, save=True):
    """
    Plots a candidate's phase-folded transit shape -- all individual
    dips overlaid into one clean averaged shape, the same array M3's
    CNN actually classifies.

    Args:
        tic_id (str): TIC ID, must be present in
            results/candidate_ids.txt (i.e. must have been folded by
            M2's fold.py)
        save (bool): if True, saves to results/plots/lc_folded_{tic_id}.png

    Returns:
        matplotlib Figure object
    """
    setup_style()

    folded_matrix, candidate_ids, periods = load_folded_data()

    if str(tic_id) not in candidate_ids:
        raise ValueError(
            f"TIC {tic_id} not found in folded candidates. This star may "
            f"not have passed detect.py's candidate threshold, or fold.py "
            f"hasn't been run on it yet."
        )

    row_idx = candidate_ids.index(str(tic_id))
    folded_flux = folded_matrix[row_idx]
    phase = np.linspace(-0.5, 0.5, len(folded_flux))

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.plot(phase, folded_flux, linewidth=1.2, color="#1f77b4")
    ax.axhline(1.0, color="gray", linestyle="--", alpha=0.5, linewidth=0.8)

    candidate_info = load_candidate_info(tic_id)
    title = f"TIC {tic_id} — Phase-folded transit"
    if candidate_info is not None:
        title += f"\nDepth: {candidate_info['depth']*100:.3f}% | Duration: {candidate_info['duration']*24:.2f}hr"

    ax.set_xlabel("Phase")
    ax.set_ylabel("Normalized flux")
    ax.set_title(title)
    plt.tight_layout()

    if save:
        os.makedirs(PLOTS_DIR, exist_ok=True)
        out_path = os.path.join(PLOTS_DIR, f"lc_folded_{tic_id}.png")
        plt.savefig(out_path, dpi=150)
        print(f"Saved {out_path}")

    return fig


def plot_all_candidates_grid(save=True):
    """
    Plots every candidate's folded transit shape in one grid image, for
    quick visual comparison across all candidates at once -- useful for
    manually screening which candidates look like real transits versus
    noise or eclipsing binaries before relying on M3's classifier.

    Args:
        save (bool): if True, saves to results/plots/all_candidates_grid.png

    Returns:
        matplotlib Figure object
    """
    setup_style()

    folded_matrix, candidate_ids, periods = load_folded_data()

    n = len(candidate_ids)
    n_cols = 5
    n_rows = int(np.ceil(n / n_cols))

    fig, axes = plt.subplots(n_rows, n_cols, figsize=(3.5 * n_cols, 2.8 * n_rows))
    axes = np.array(axes).flatten()

    for i, tic_id in enumerate(candidate_ids):
        ax = axes[i]
        ax.plot(folded_matrix[i], linewidth=0.8, color="#1f77b4")
        ax.set_title(f"TIC {tic_id}", fontsize=9)
        ax.set_xticks([])
        ax.set_yticks([])

    for j in range(n, len(axes)):
        axes[j].axis("off")

    plt.tight_layout()

    if save:
        os.makedirs(PLOTS_DIR, exist_ok=True)
        out_path = os.path.join(PLOTS_DIR, "all_candidates_grid.png")
        plt.savefig(out_path, dpi=120)
        print(f"Saved {out_path}")

    return fig


