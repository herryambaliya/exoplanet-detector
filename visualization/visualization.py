def load_candidate_info(tic_id):
    """
    Looks up a candidate's BLS result (period, t0, power, depth,
    duration) from M2's results/candidates.csv.

    Returns:
        dict, or None if this TIC_ID is not in candidates.csv
    """
    if not os.path.exists(CANDIDATES_PATH):
        return None
    with open(CANDIDATES_PATH, "r") as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row["TIC_ID"] == str(tic_id):
                return {
                    "period": float(row["period_days"]),
                    "power": float(row["power"]),
                    "depth": float(row["depth"]),
                    "duration": float(row["duration_days"]),
                    "t0": float(row["t0"]),
                }
    return None


