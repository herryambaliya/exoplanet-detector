def setup_style():
    """
    Sets consistent matplotlib styling for every plot in this project,
    so the whole PPT looks like it came from one coherent system rather
    than four different people's individual styles.

    Call this once at the top of any script that makes plots, before
    any plt.subplots() or plt.figure() call.
    """
    plt.rcParams["figure.dpi"] = 150
    plt.rcParams["font.size"] = 11
    plt.rcParams["axes.spines.top"] = False
    plt.rcParams["axes.spines.right"] = False
    plt.rcParams["figure.facecolor"] = "white"
    plt.rcParams["axes.facecolor"] = "white"
    plt.rcParams["axes.grid"] = True
    plt.rcParams["grid.alpha"] = 0.25


def load_folded_data():
    """
    Loads the folded candidates file, handling both the old format
    (plain numpy array, shape N x 200) and the new bundled format
    M3 requested (dict with tic_ids, fluxes, periods keys).

    Returns:
        tuple: (folded_matrix, candidate_ids, periods)
            folded_matrix: np.ndarray shape (N, 200)
            candidate_ids: list of str TIC IDs
            periods: np.ndarray shape (N,) or None if not available
    """
    if not os.path.exists(FOLDED_PATH):
        raise FileNotFoundError(
            "results/folded_candidates.npy not found. "
            "Run M2's fold.py (fold_all_candidates()) first."
        )

    raw = np.load(FOLDED_PATH, allow_pickle=True)

    # New bundled format: raw is a 0-d array containing a dict
    if raw.ndim == 0 and isinstance(raw.item(), dict):
        bundle = raw.item()
        folded_matrix = bundle["fluxes"]
        candidate_ids = list(bundle["tic_ids"])
        periods = bundle.get("periods", None)
        return folded_matrix, candidate_ids, periods

